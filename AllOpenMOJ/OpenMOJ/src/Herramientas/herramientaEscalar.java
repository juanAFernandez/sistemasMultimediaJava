    private void botonMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMasActionPerformed
        this.escala+=0.1;
        this.factorescala.setText(Double.toString(escala));
        aplicar();
        this.tamImagen.setText(TMP.getWidth()+" ancho x "+TMP.getHeight()+" alto");
    }//GEN-LAST:event_botonMasActionPerformed

    private void botonMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMenosActionPerformed
        this.escala-=0.1;

        if(escala<0.1){
             JOptionPane.showMessageDialog(this, "No puede hacer la imagen tan pequeña. No la verás.", "Algo pequeño", JOptionPane.WARNING_MESSAGE);
             escala=0.1;
             this.factorescala.setText("0.1");            
        }
        this.factorescala.setText(Double.toString(escala));
        aplicar();
        this.tamImagen.setText(TMP.getWidth()+" ancho x "+TMP.getHeight()+" alto");
    }//GEN-LAST:event_botonMenosActionPerformed

